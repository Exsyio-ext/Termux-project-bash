#!/bin/bash

echo "
      .########.##.....##..######..##....##.####..#######.
      .##........##...##..##....##..##..##...##..##.....##
      .##.........##.##...##.........####....##..##.....##
      .######......###.....######.....##.....##..##.....##
      .##.........##.##.........##....##.....##..##.....##
      .##........##...##..##....##....##.....##..##.....##
      .########.##.....##..######.....##....####..#######.
        "
echo -e "Scan time\n$(date)"

echo ""
echo -n " INPUT YOUR TARGET : "
    read i
echo ""
echo ""
echo " + YOUR TARGET "
echo "------------- "
echo "-------------------------------------------------------------- "
echo ""
echo $i
echo ""
                    ping -c 1 $i | awk '/PING/ { print $3 }' | tr -d "()" | tee -a test.txt

echo ""
echo " + IP INFO "
echo "-----------"
echo "-------------------------------------------------------------- "
echo ""
     for ip in $( cat test.txt ); do
  		curl -s http://ipinfo.io/$ip | tr -d '"''{}' | sed 's/^ */ /g'  
 		rm test.txt ; done
echo ""
echo " + SERVER NAME "
echo "------------- "
echo "-------------------------------------------------------------- "
echo ""
       for ip in i ; do
         host -t ns $i
echo ""
echo " + PORT OPEN "
echo "------------- "
echo "-------------------------------------------------------------- "
echo ""
        nmap $i | sed -e '1,5d' | sed 's/N.*/ /g'

echo ""
echo " + SUBDOMAIN "
echo "------------- "
echo "-------------------------------------------------------------- "
echo ""
         curl -s  https://certspotter.com/api/v0/certs\?domain\=$i | jq '.[].dns_names[]' | tr -d '"' | sed 's/www.//g' | sort -u
echo ""
echo "-------------------------------------------------------------- " ; done
